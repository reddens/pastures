<h3>  SIWES Program</h3>
<p>The Student Industrial Work Experience Scheme (SIWES) program was established by the Industrial Trust Fund (ITF) in <strong>1973</strong> and it is the accepted skill training program adopted by many Nigerian Tertiary Institutions, it forms part of the approved minimum academic standard in various degree program in these institutes for graduating its student. <br />
The SIWES program is an effort to bridge the gap that exists between theory and practice.<br />
<em><strong>The SIWES activities a Student is expectd to Perform is drafted thus</strong></em>
<h3>THE AIM AND OBJECTIVE OF SIWES to Students</h3>
The main aim to be achieved during the SIWES program is the ability to practice the knowledge gained in the school room into an experience gained on the industrial field within the range of the student's course of study. The key objectives are; 
<br />
<ul type="square">
<li>Provide students with an opportunity to apply their theoretical knowledge gain in the school room in real work situation on the field.</li>
<li>To create a better and easier transmission for the student, form university student, to working class adults. Thus enhance student contact for later job placement.</li>
<li>To give room for social and academic interaction with professionals in their fields and working environment and also to expose the reality of working places.</li>
<li>To in bide into each student the ability to create a good work relationship with their present student colleagues and also their future work colleagues</li>
</ul>
